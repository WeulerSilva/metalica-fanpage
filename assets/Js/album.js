function createDiv(className) {
  const div = document.createElement('div');
  div.classList.add(className);
  return div;
}

for (const key in album) {
  if (album.hasOwnProperty(key)) {
    const element = album[key];
    
    const albumDiv = createDiv('album');
    
    const cover = createDiv('cover');
    albumDiv.appendChild(cover);

    const releaseDiv = createDiv('album-release');
    if(languageEng) {
      releaseDiv.textContent = element.release;
    }else {
      releaseDiv.textContent = element.lancamento;
    }
    
    albumDiv.appendChild(releaseDiv);

    const titleDiv = createDiv('album-title');
    titleDiv.textContent = element.title;
    albumDiv.appendChild(titleDiv);

    cover.style.backgroundImage = `url(./assets/Images/${element.img}.jfif)`
    document.querySelector('.albums-cover').appendChild(albumDiv);




    albumDiv.addEventListener('click', () => {
      document.querySelector('.albums-cover').style.display = 'none';
      document.querySelector('.album-desc').style.display = 'flex';
      document.querySelector('.album-back').style.display = 'flex';

      document.querySelector('.cover-desc').style.backgroundImage = `url(./assets/Images/${element.img}.jfif)`;

      document.querySelector('.listen-desc').addEventListener('click', () => {
        window.open(element.link)
      });

      document.querySelector('.title-desc').innerHTML = element.title;
      document.querySelector('.bass-desc span').innerHTML = element.bass;

      if(languageEng) {
        document.querySelector('.album-info').innerHTML = element.info;
      }else {
        document.querySelector('.album-info').innerHTML = element.infoBr;
      }

      const songMusicList = document.querySelectorAll('.song-music');
      songMusicList.forEach((songDiv) => {
        songDiv.parentNode.removeChild(songDiv);
      });


      const albumSong = element.songs;

      for(let i = 0; i < albumSong.length; i++) {

        const songDiv = createDiv('song-music');
        songDiv.textContent = `${i+1}: ${albumSong[i]}`;

        document.querySelector('.music-desc').appendChild(songDiv);
      }

    })
  }
}



document.querySelector('.album-back').addEventListener('click', () => {
  document.querySelector('.albums-cover').style.display = 'flex';
  document.querySelector('.album-desc').style.display = 'none';
  document.querySelector('.album-back').style.display = 'none';
  document.querySelectorAll('.song-title').innerHTML = '';
});





